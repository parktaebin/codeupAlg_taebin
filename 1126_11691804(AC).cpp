#include <iostream>

using namespace std;
int main()
{
	int iFirstNum, iSecondNum;
	cin >> iFirstNum >> iSecondNum;

	cout << iFirstNum <<" + "<< iSecondNum <<" = "<< iFirstNum + iSecondNum << endl;
	cout << iFirstNum <<" - "<< iSecondNum <<" = "<< iFirstNum - iSecondNum << endl;
	cout << iFirstNum <<" * "<< iSecondNum <<" = "<< iFirstNum * iSecondNum << endl;
	cout << iFirstNum <<" / "<< iSecondNum <<" = "<< iFirstNum / iSecondNum << endl;
	cout << iFirstNum <<" % "<< iSecondNum <<" = "<< iFirstNum % iSecondNum << endl;

}

